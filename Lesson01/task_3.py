"""
Задание 3. По введенным пользователем координатам двух
точек вывести уравнение прямой,
проходящей через эти точки.

"""
x_A = 4
y_A = 2
x_B = -3
y_B = 1
# =========k=============
k = (y_B - y_A) / (x_B - x_A)
# ==========b=============
# b = y_A - k * x_A
b = y_B - k * x_B

# ==========формула ==================
print(f'Уравнение прямой, проходящей через эти точки: y = {round(k,2)}x + {round(b,2)}')